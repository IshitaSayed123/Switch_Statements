package switch_condition;
public class tas4 {
	public static void main(String[] args) {
		String S="day";
		switch(S) {
		    case "Monday": System.out.println("Today's date is 01-jan-2026");
		    break;
		    case "Tuesday": System.out.println("Today's date is 02-jan-2026");
		    break;
		    case "Wednesday": System.out.println("Today's date is 03-jan-2026");
		    break;
		    case "Thursday": System.out.println("Today's date is 04-jan-2026");
		    break;
		    case "Friday": System.out.println("Today's date is 05-jan-2026");
		    break;
		    case "Saturday": System.out.println("Today's date is 06-jan-2026");
		    break;
		    default: System.out.println("Today's date is Invalid");
		}
	}
}
